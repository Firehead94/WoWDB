	QUEST_POS_Y INT,
	QUEST_POS_Z INT,
	QUEST_REPEATABLE BIT DEFAULT '0', -- LEAVE 0 UNLESS DAILY/WEEKLY IS SET IN FLAG
	QUEST_SUGGESTED_PLAYERS INT, -- AMMOUNT OF PLAYERS RECOMMENDED TO COMPLETE
	FOREIGN KEY (QUEST_PREVQUEST) REFERENCES QUEST (QUEST_ID),
	FOREIGN KEY (QUEST_NEXTQUEST) REFERENCES QUEST (QUEST_ID),
	CONSTRAINT CK_QUEST_ID CHECK (QUEST_ID > 0)
);
