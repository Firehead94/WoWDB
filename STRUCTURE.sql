-- Start structure for Quest

CREATE TABLE QUEST (
	QUEST_ID INT IDENTITY PRIMARY KEY,
	QUEST_ZONE INT,
	GO_ID INT FOREIGN KEY REFERENCES GAMEOBJECT,
	QUEST_SORT SMALLINT NOT NULL, -- SORTING AREA IN CHARACTERS QUEST LOG, SEE BELOW
	QUEST_FLAG SMALLINT NOT NULL, -- TYPE OF QUEST,  SEE BELOW
	QUEST_MINLEVEL SMALLINT DEFAULT '1', -- LEVEL AT WHICH A CHARACTER CAN ACCEPT THIS QUEST
	QUEST_LEVEL SMALLINT, -- LEVEL RECOMMENDED FOR COMPLETION
	QUEST_REQ_RACE SMALLINT, -- REFERS TO CHARACTER RACE, SEE CHARACTER TABLE COMMENTS
	QUEST_REQ_CLASS SMALLINT, -- REFERS TO CHARACTER CLASS, SEE CHARACTER TABLE COMMENTS
	QUEST_REQ_SKILL SMALLINT, -- REFERS TO CHARACTER SKILL, SEE ITEMS TABLE COMMENTS
	QUEST_REQ_SKILL_LEVEL SMALLINT, -- REFERS TO LEVEL OF CHARACTER SKILL
	QUEST_REQ_REP SMALLINT, -- REFERS TO A FACTION ID HELD BY CLIENT
	QUEST_REQ_REP_LEVEL INT, -- REFERS TO STANDING WITH FACTION (NEUTRAL, HONORED, REVERED, EXAULTED)
	QUEST_LIMITTIME TIME, -- TIME LIMIT FOR COMPLETION
	QUEST_PREVQUEST INT FOREIGN KEY REFERENCES QUEST (QUEST_ID), -- APPLICABLE IN CHAIN OF QUESTS
	QUEST_NEXTQUEST INT FOREIGN KEY REFERENCES QUEST (QUEST_ID), -- APPLICABLE IN CHAIN OF QUESTS
	QUEST_DETAILS VARCHAR(255) NOT NULL, -- SHOWN TO CHARACTERS IN QUEST LOG
	QUEST_TITLE VARCHAR(255) NOT NULL, -- DISLAYED TO CHARACTERS
	QUEST_OBJECTIVE VARCHAR(255) NOT NULL, -- SHOW TO CHARACTERS IN QUEST LOG, WHAT MUST BE DONE
	QUEST_COMPLETE_TXT VARCHAR(255) NOT NULL, -- TEXT SHOWN BY NPC WHEN COMPLETED
	QUEST_INCOMPLETE_TXT VARCHAR(255) NOT NULL, -- TEXT SHOWN BY NPC WHEN NOT COMPLETED
	QUEST_END_TXT VARCHAR(255) NOT NULL, -- TEXT SHOWN BY NPC WHEN TURNING IN
	QUEST_OBJECTIVE_TXT1 VARCHAR(255) NOT NULL, -- WHAT MUST BE DONE (SHORTEND VERSION) IN ORDER TO COMPLETE QUEST
	QUEST_OBJECTIVE_TXT2 VARCHAR(255), -- FOR MULTIPLE OBJECTIVES
	QUEST_OBJECTIVE_TXT3 VARCHAR(255),
	QUEST_OBJECTIVE_TXT4 VARCHAR(255),
	QUEST_MAILTEMP VARCHAR(255), -- WHAT LETTER WILL SAY IF MAIL IS DELIVERED AT QUEST COMPLETION
	QUEST_MAILDELAY TIME, -- HOW LONG IT TAKES FOR MAIL TO ARRIVE
	QUEST_MAILSENDITEM INT, -- ITEM ATTACHED TO MAIL IF APPLICABLE
