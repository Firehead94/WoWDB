/* DML */

-- 3 TABLE JOIN, ORDER BY, GROUP BY, AGGREGATE
SELECT B.BNET_ID AS "BNET ACCOUNT NUMBER",
 ACCT_NAME AS "ACCOUNT NAME",
 BNET_EMAIL AS "EMAIL ADDRESS",
 BNET_FNAME AS "FIRST NAME",
 BNET_LNAME AS "LAST NAME", 
 COUNT(*) AS "NUMBER OF CHARACTERS"
FROM BNET_ACCOUNT B, ACCOUNT A, CHARACTERS C
WHERE (B.BNET_ID = A.BNET_ID AND A.ACCT_ID = C.ACCT_ID)
GROUP BY B.BNET_ID, ACCT_NAME, BNET_EMAIL, BNET_FNAME, BNET_LNAME
ORDER BY B.BNET_ID

-- 2 TABLE JOIN, LOGICAL
SELECT A.ACCT_ID AS "ACCOUNT NUMBER",
 C.CHAR_ID AS "CHARACTER NUMBER",
 CHAR_NAME AS "CHARACTER NAME"
FROM ACCOUNT A, CHARACTERS C
WHERE (A.ACCT_ID = C.ACCT_ID) AND ACCT_DEFAULT_REALM = 'EMERALD DREAM'

-- SUBQUERY, AGGREGATE
SELECT CHAR_ID, CHAR_NAME, CHAR_KILL_LIFETIME AS "TOTAL KILLS"
FROM CHARACTERS
WHERE CHAR_KILL_LIFETIME = (SELECT MAX(CHAR_KILL_LIFETIME) FROM CHARACTERS)

-- NULL MANIPULATION
SELECT QUEST_ID, QUEST_TITLE
FROM QUEST
WHERE GO_ID IS NOT NULL

-- AGGREGATE, 3 TABLE JOIN, COMPARISON 
SELECT ITEM_NAME,
 ITEM_LEVEL AS "ITEM LEVEL",
 ITEM_REQ_LEVEL AS "CHARACTER LEVEL",
 I.ITEM_ID AS "ITEM ID",
 CI.CHAR_ID AS "CHARACTER ID",
 C.CHAR_NAME AS "CHARACTER NAME"
FROM CHARACTERS C, CHAR_ITEMS CI, ITEMS I
WHERE (C.CHAR_ID = CI.CHAR_ID AND I.ITEM_ID = CI.ITEM_ID) AND ITEM_QUALITY >= 4
GROUP BY ITEM_LEVEL, ITEM_REQ_LEVEL, ITEM_NAME, I.ITEM_ID, CI.CHAR_ID, C.CHAR_NAME

-- MATH, 3 TABLE JOIN
SELECT B.BNET_ID AS "BNET ACCT NUMBER",
SUM(CHAR_MONEY/10000) AS "AMOUNT OF GOLD"
FROM BNET_ACCOUNT B, ACCOUNT A, CHARACTERS C
WHERE B.BNET_ID = A.BNET_ID AND A.ACCT_ID = C.ACCT_ID
GROUP BY B.BNET_ID

